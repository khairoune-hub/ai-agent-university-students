async function seed() {
  console.log('[seed] Inserting default AI settings...');
  await pool.query(
    `INSERT INTO ai_settings (id, system_prompt, model, temperature)
     VALUES (1, $1, $2, $3)
     ON CONFLICT (id) DO NOTHING`,
    [DEFAULT_SYSTEM_PROMPT, DEFAULT_MODEL, DEFAULT_TEMPERATURE]
  );

  const { rows } = await pool.query<{ count: string }>('SELECT count(*)::int AS count FROM articles');
  if (parseInt(rows[0].count, 10) > 0) {
    console.log('[seed] Articles already present — skipping sample articles.');
  } else {
    console.log(`[seed] Inserting ${SAMPLE_ARTICLES.length} sample articles...`);
    for (const a of SAMPLE_ARTICLES) {
      await pool.query(
        'INSERT INTO articles (title, category, content) VALUES ($1, $2, $3)',
        [a.title, a.category, a.content]
      );
    }
  }

  console.log('[seed] Done.');
}

seed()
  .then(() => pool.end())
  .then(() => process.exit(0))
  .catch((err) => {
    console.error('[seed] Failed:', err);
    pool.end().finally(() => process.exit(1));
  });
